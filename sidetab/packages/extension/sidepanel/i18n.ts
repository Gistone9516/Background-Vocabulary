const STRINGS: Record<OutputLocale, Dict> = { ko, en, ja, zh };

// 표시 문자열을 가져온다. {x} 자리는 params로 치환한다. 누락 키는 ko로 폴백.
export function t(locale: OutputLocale, key: string, params?: Record<string, string | number>): string {
  const s = STRINGS[locale]?.[key] ?? STRINGS.ko[key] ?? key;
  if (!params) return s;
  return s.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}

